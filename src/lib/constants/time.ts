// -- CONSTANTS -- //

const SECOND = 1000;
const MINUTE = SECOND * 60;
const THIRTY_MINUTES = MINUTE * 30;
const HOUR = MINUTE * 60;
const DAY = HOUR * 24;
const WEEK = DAY * 7;
const MONTH = DAY * 30;
const YEAR = DAY * 365;

export const TIME = {
	SECOND,
	MINUTE,
	THIRTY_MINUTES,
	HOUR,
	DAY,
	WEEK,
	MONTH,
	YEAR,
};
